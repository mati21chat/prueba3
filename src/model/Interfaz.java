    private void BborrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BborrarActionPerformed
         Connection conexion;
        PreparedStatement ps;
        
        try{
            
            conexion = getConexion();
            ps = (PreparedStatement) conexion.prepareStatement("DELETE FROM profesor WHERE IdProfesor=?");
            ps.setString(1, JOptionPane.showInputDialog("Ingrese el numero de idProfesor a eliminar"));
            
            
            int result = ps.executeUpdate();
            
            if(result>0){
                JOptionPane.showMessageDialog(null, "Registro eliminado de manera exitosa");
                cargarTabla();
                limpiar();
            }
            
            else{
                JOptionPane.showMessageDialog(null, "Registro no eliminado");  
            }
            
            conexion.close();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error "+ex);
        }
    }//GEN-LAST:event_BborrarActionPerformed

