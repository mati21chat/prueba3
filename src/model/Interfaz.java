    private void BinsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BinsertarActionPerformed
       
        Connection conexion;
        PreparedStatement ps;
        
        try{
            
            conexion = getConexion();
            ps = (PreparedStatement) conexion.prepareStatement("INSERT INTO profesor VALUES (?,?,?,?,?,?)");
            ps.setString(1, campoIdProfesor.getText());
            ps.setString(2, campoTitulo.getText());
            ps.setString(3, campoDepartamento.getText());
            ps.setString(4, campoNombre.getText());
            ps.setString(5, campoApellido.getText());
            ps.setString(6, campoDireccion.getText());
             
            int result = ps.executeUpdate();
            
            if(result>0){
                JOptionPane.showMessageDialog(null, "Registro insertado correctamente");
                cargarTabla();
                limpiar();
            }
            
            else{
                JOptionPane.showMessageDialog(null, "Registro no insertado");  
            }
            
            conexion.close();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error "+ex);
        }
        
             
    }//GEN-LAST:event_BinsertarActionPerformed

